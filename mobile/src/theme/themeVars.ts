/** Ready-to-apply nativewind style objects, one per theme. */
export const themeVars = Object.fromEntries(
  (Object.keys(THEME_TOKENS) as ThemeId[]).map((id) => [
    id,
    vars({ ...BRAND, ...THEME_TOKENS[id] }),
  ])
) as Record<ThemeId, ReturnType<typeof vars>>;

/**
 * Raw HSL triplet lookup for places that need a literal color value
 * (e.g. navigation chrome, gradients, shadows built in JS).
 */
export function themeToken(theme: ThemeId, token: string): string {
  return THEME_TOKENS[theme][token] ?? BRAND[token as keyof typeof BRAND] ?? '';
}

/** `hsl()` color string for a token of the given theme. */
export function themeHsl(theme: ThemeId, token: string, alpha?: number): string {
  const triplet = themeToken(theme, token);
  if (!triplet) return 'transparent';
  return alpha !== undefined
    ? `hsla(${triplet.split(' ').join(', ')}, ${alpha})`
    : `hsl(${triplet.split(' ').join(', ')})`;
}
